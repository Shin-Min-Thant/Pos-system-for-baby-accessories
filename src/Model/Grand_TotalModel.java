package Model;

public class Grand_TotalModel {
	private String grand_purchase;
    private String grand_delivery;
    private String grand_sale;
    private String grand_order;
    private String loss;
    private String profit;
    private String month;
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getLoss() {
		return loss;
	}
	public void setLoss(String loss) {
		this.loss = loss;
	}
	public String getProfit() {
		return profit;
	}
	public void setProfit(String profit) {
		this.profit = profit;
	}
	public String getGrand_purchase() {
		return grand_purchase;
	}
	public void setGrand_purchase(String grand_purchase) {
		this.grand_purchase = grand_purchase;
	}
	public String getGrand_delivery() {
		return grand_delivery;
	}
	public void setGrand_delivery(String grand_delivery) {
		this.grand_delivery = grand_delivery;
	}
	public String getGrand_sale() {
		return grand_sale;
	}
	public void setGrand_sale(String grand_sale) {
		this.grand_sale = grand_sale;
	}
	public String getGrand_order() {
		return grand_order;
	}
	public void setGrand_order(String grand_order) {
		this.grand_order = grand_order;
	}
}
